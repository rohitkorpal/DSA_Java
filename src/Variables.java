public class Variables {
    static void main() {

// Java Variables
//        - A **variable** is a named memory location used to store data.
//        - Must have: **data type + unique name + (optional) value**.
//
// Example: Declaration + Assignment

//        int problemsSolved = 120;   // int → type, problemsSolved → name, 120 → value

// Separate Declaration + Assignment

//        double completionRate;
//        completionRate = 87.5;
//
//        boolean isPremiumMember = true;

// Rules
//        - Java is **statically typed** → must declare type before use.
//        - Value can change, but **type cannot change**.

//        int rank = 450;
//        rank = 320;   // ✅ value updated
//
//        int streak = 30;
//        double streak = 30.5;   // ❌ redeclaration with different type not allowed

// Naming Rules
//        - Can use letters, digits, `_`, `$`.
//        - Cannot start with a digit.
//        - Cannot use Java keywords.
//        - Case-sensitive (`score` vs `Score` are different).

// Java Literals
//        A **literal** is the fixed value assigned to a variable.
//
// Integer Literals
//        - **Decimal (Base 10):**
//        int enrolledStudents = 250;

//        - **Binary (Base 2):**
//        int accessMask = 0b10101;   // 0b prefix

//        - **Octal (Base 8):**
//        int filePermission = 075;   // leading 0

//        - **Hexadecimal (Base 16):**
//        int themeColor = 0xFF;      // 0x prefix

// Floating-Point Literals
//        double averageScore = 78.6;
//        float discountPercent = 15.5F;   // F required for float

//        - By default, decimals are `double`.
//        - Scientific notation supported:

//        double serverLoad = 2.5e3;   // 2500

// Character Literals
//        char grade = 'A';
//        char newLine = '\n';
//        char tabSpace = '\t';

// String Literals
//        String platformName = "CodeHelp ONE";
//        String courseTrack = "DSA + Core + Aptitude";

//  Boolean Literals
//        boolean isLoggedIn = true;
//        boolean hasCompletedSheet = false;


    }
}
